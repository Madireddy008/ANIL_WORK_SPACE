#Numeric DataTypes
# Float variables
float1 = 5.0
float2 = 2.5

# Integer variables
int1 = 10
int2 = 5

# Basic Arithmetic
result1 = float1 + float2
print("Addition:", result1)

result2 = float1 - float2
print("Subtraction:", result2)

result3 = float1 * float2
print("Multiplication:", result3)

result4 = float1 / float2
print("Division:", result4)

# Rounding
result5 = round(3.14159265359, 2)  # Rounds to 2 decimal places
print("Rounded:", result5)

# Integer Division
result1 = int1 // int2
print("Integer Division:", result1)

# Modulus (Remainder)
result2 = int1 % int2
print("Modulus (Remainder):", result2)

# Absolute Value
result3 = abs(-7)
print("Absolute Value:", result3)