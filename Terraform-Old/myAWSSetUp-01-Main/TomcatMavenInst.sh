sudo apt install unzip -y
unzip /opt/apache-maven-3.9.5-bin.zip -d /opt
sudo chmod 777 /etc/profile
sudo cat > /etc/profile << EOF
# /etc/profile: system-wide .profile file for the Bourne shell (sh(1))
# and Bourne compatible shells (bash(1), ksh(1), ash(1), ...).

if [ "${PS1-}" ]; then
  if [ "${BASH-}" ] && [ "$BASH" != "/bin/sh" ]; then
    # The file bash.bashrc already sets the default PS1.
    # PS1='\h:\w\$ '
    if [ -f /etc/bash.bashrc ]; then
      . /etc/bash.bashrc
    fi
  else
    if [ "`id -u`" -eq 0 ]; then
      PS1='# '
    else
      PS1='$ '
    fi
  fi
fi

if [ -d /etc/profile.d ]; then
  for i in /etc/profile.d/*.sh; do
    if [ -r $i ]; then
      . $i
    fi
  done
  unset i
fi
export M2_HOME=/opt/apache-maven-3.9.5/
export PATH=/usr/local/sbin:/usr/local/bin:/usr/sbin:/usr/bin:/sbin:/bin:/usr/games:/usr/local/games:/snap/bin:/opt/apache-maven-3.9.5/bin
EOF

source /etc/profile
sudo chmod 644 /etc/profile
sudo chown root:root /etc/profile
mvn --version
git clone https://github.com/anilmadireddy/myWebapp.git
sudo mv myWebapp /home/ubuntu/
sudo chown ubuntu:ubuntu -R /home/ubuntu/myWebapp
cd /home/ubuntu/myWebapp/ && mvn clean package
